// Функция для получения стихов по главе
export function getPoemsByChapter(chapterId: string): Poem[] {
  return poems.filter(poem => poem.chapterId === chapterId);
}

// Функция для получения стиха по ID
export function getPoemById(id: string): Poem | undefined {
  return poems.find(poem => poem.id === id);
}

// Функция для получения всех стихов в порядке глав
export function getAllPoemsOrdered(chapterOrder: string[]): Poem[] {
  return chapterOrder.flatMap(chapterId => getPoemsByChapter(chapterId));
}
