export const getTotalPages = (): number => pages.length;

export const getPageByNumber = (number: number): PoemPage | undefined => 
  pages.find(p => p.number === number);

export const getPageById = (id: string): PoemPage | undefined => 
  pages.find(p => p.id === id);

export const getPageIndex = (id: string): number => 
  pages.findIndex(p => p.id === id);

export const getNextPage = (currentId: string): PoemPage | undefined => {
  const currentIndex = getPageIndex(currentId);
  return currentIndex >= 0 && currentIndex < pages.length - 1 
    ? pages[currentIndex + 1] 
    : undefined;
};

export const getPrevPage = (currentId: string): PoemPage | undefined => {
  const currentIndex = getPageIndex(currentId);
  return currentIndex > 0 
    ? pages[currentIndex - 1] 
    : undefined;
};

